package db;

/**
 *
 * @author Usuario
 */
public class Habitacion {
    
    private int id;
    private String titulo;
    private String descripcion;
    private String pais;
    private String ciudad;
    private String imagen;

    public Habitacion(int id, String titulo, String descripcion, String pais, String ciudad,  String imagen) {
        this.id = id;
        this.titulo = titulo;
        this.descripcion = descripcion;
        this.pais = pais;
        this.ciudad = ciudad;
        this.imagen = imagen;
    }
    
    public Habitacion() {
    }

    public String getImagen() {
        return imagen;
    }

    public void setImagen(String imagen) {
        this.imagen = imagen;
    }
    
    @Override
    public String toString() {
        return "Habitacion{" + "id=" + id + ", titulo=" + titulo + ", descripcion=" + descripcion + ", pais=" + getPais() + ", ciudad=" + getCiudad() + ", imagen=" + getImagen() + '}';
    }
    
    /**
     * @return the id
     */
    public int getId() {
        return id;
    }

    /**
     * @param id the id to set
     */
    public void setId(int id) {
        this.id = id;
    }

    /**
     * @return the titulo
     */
    public String getTitulo() {
        return titulo;
    }

    /**
     * @param titulo the titulo to set
     */
    public void setTitulo(String titulo) {
        this.titulo = titulo;
    }

    /**
     * @return the descripcion
     */
    public String getDescripcion() {
        return descripcion;
    }

    /**
     * @param descripcion the descripcion to set
     */
    public void setDescripcion(String descripcion) {
        this.descripcion = descripcion;
    }

    /**
     * @return the pais
     */
    public String getPais() {
        return pais;
    }

    /**
     * @param pais the pais to set
     */
    public void setPais(String pais) {
        this.pais = pais;
    }

    /**
     * @return the ciudad
     */
    public String getCiudad() {
        return ciudad;
    }

    /**
     * @param ciudad the ciudad to set
     */
    public void setCiudad(String ciudad) {
        this.ciudad = ciudad;
    }

    
}
