    private void imageActionPerformed(java.awt.event.ActionEvent evt) {// GEN-FIRST:event_imageActionPerformed
        // return if there is no image url or is invalid (not a url)
        if (image.getText().isEmpty() || !image.getText().startsWith("http")) {
            return;
        }
        // try to load the image
        try {
            ImageIcon imageIcon = new ImageIcon(new URL(image.getText()));
            imageIcon.setImage(imageIcon.getImage().getScaledInstance(250, 250, java.awt.Image.SCALE_SMOOTH));
            imgPreload.setIcon(imageIcon);
        } catch (Exception e) {
            System.out.println("Error loading image: " + e.getMessage());
            imgPreload.setText("Error loading image");
        }
    }// GEN-LAST:event_imageActionPerformed

    private void guardarActionPerformed(java.awt.event.ActionEvent evt) {// GEN-FIRST:event_guardarActionPerformed
        if (title.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "El ti√∫lo no puede estar vac√≠o", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        if (description.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "La descripci√≥n no puede estar vac√≠o", "Error",
                    JOptionPane.ERROR_MESSAGE);
            return;
        }
        if (image.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "La imagen no puede estar vac√≠o", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        if (pais.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "El pais no puede estar vac√≠o", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        if (ciudad.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "La ciudad no puede estar vac√≠o", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        Habitacion room = new Habitacion(0, title.getText(), description.getText(), pais.getText(),
                ciudad.getText(), image.getText());
        boolean res=db.SaveRoom(room);
        if (!res) {
            JOptionPane.showMessageDialog(this, "Error guardando la habitaci√≥n", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        JOptionPane.showMessageDialog(this, "Habitaci√≥n guardada", "Exito", JOptionPane.INFORMATION_MESSAGE);
        this.parent.setVisible(true);
        this.parent.LoadRooms();
        this.dispose();
    }// GEN-LAST:event_guardarActionPerformed

